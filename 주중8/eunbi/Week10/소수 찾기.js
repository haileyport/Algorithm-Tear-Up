function solution(n) {
  let arr = new Array(n+1).fill(true);
  let count = 0;
  
  arr[0] = false;
  arr[1] = false;
  
  const sqrt = Math.floor(Math.sqrt(n));
  
  for(let i = 2; i <= sqrt; i++){
      if(arr[i]){
          num = 2; 
          
          while(i*num <= n){
              arr[i*num] = false;
              num++;
          }
      }
  }
  
  arr.forEach(el => {
      if(el) count++;
  })
  
  return count;
  
}